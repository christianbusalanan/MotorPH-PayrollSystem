    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
    String username = txtUsername.getText().trim();
    String password = new String(txtPassword.getPassword()).trim();

    if (username.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Username or Password cannot be empty.", "Login Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    String role = Database.verifyLogin(username, password);

    if (role != null) {
        JOptionPane.showMessageDialog(this, "Login Successful!");

        final String loggedInUsername = username;

        switch (role) {
            case "HR Manager" -> new HRManagerDB().setVisible(true);
            case "Employee" -> java.awt.EventQueue.invokeLater(() -> new EmployeeDB(loggedInUsername).setVisible(true)); // âœ… Corrected
            case "Payroll Staff" -> new PayrollStaffDB().setVisible(true);
            default -> {
                JOptionPane.showMessageDialog(this, "Unknown role: " + role, "Login Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }

        this.dispose(); // Close login form after successful login
    } else {
        JOptionPane.showMessageDialog(this, "Invalid username or password. Please try again.", "Login Failed", JOptionPane.ERROR_MESSAGE);
        txtPassword.setText(""); // Clear only password field
    }
    }//GEN-LAST:event_btnLoginActionPerformed

